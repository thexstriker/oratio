# Align the predicted and correct phonemes
def align_phonemes(predicted, correct):
    # Split both the predicted and correct phonemes into lists
    predicted_split = predicted.split()  # A list of phonemes in predicted transcription
    correct_split = correct.split()      # A list of phonemes in correct transcription

    # Initialize an empty alignment list
    alignment = []

    # Align the predicted phonemes with correct phonemes
    i, j = 0, 0
    while i < len(predicted_split) and j < len(correct_split):
        print(predicted_split[i], correct_split[j])
        if predicted_split[i] == correct_split[j]:
            alignment.append((correct_split[j], "correct"))
            i += 1
            j += 1
        else:
            alignment.append((predicted_split[i], "incorrect"))
            i += 1
    while i < len(predicted_split):
        alignment.append((predicted_split[i], "incorrect"))
        i += 1
    return alignment

alignment = align_phonemes(predictedPhoneme_transcription, correct_phonemes)
