# Phoneme-level Alignment and Time Frame Assignment
def phoneme_time_frames(logits, audio_duration, predicted_phonemes):
    phonemes = predicted_phonemes.split()  # Split predicted transcription into phonemes
    time_per_token = audio_duration / logits.shape[1]  # Calculate time per token (frame)
    phoneme_times = []

    # Align phonemes with time frames
    for idx, phoneme in enumerate(phonemes):
        start_time = idx * time_per_token
        end_time = (idx + 1) * time_per_token
        phoneme_times.append((phoneme, start_time, end_time))
    return phoneme_times

