# Step 2: Load Pre-trained Wave2Vec2.0 Model and Processor
model_name = "facebook/wav2vec2-base-960h"  # You can replace this with a different model
processor = Wav2Vec2Processor.from_pretrained(model_name)
model = Wav2Vec2ForCTC.from_pretrained(model_name)

# Step 3: Load and Process Audio File
def load_audio(file_path, target_sampling_rate=16000):
    # Load the audio file
    waveform, sample_rate = torchaudio.load(file_path)
    # Resample if necessary
    if sample_rate != target_sampling_rate:
        resampler = torchaudio.transforms.Resample(orig_freq=sample_rate, new_freq=target_sampling_rate)
        waveform = resampler(waveform)
    # Convert to single channel (mono) if needed
    if waveform.shape[0] > 1:
        waveform = torch.mean(waveform, dim=0, keepdim=True)
    return waveform.squeeze(), target_sampling_rate

audio_file = "audio_samples/recording.wav"  # Path to your recorded audio
waveform, sample_rate = load_audio(audio_file)

# Step 4: Tokenize the Audio
input_values = processor(waveform, sampling_rate=sample_rate, return_tensors="pt", padding=True).input_values

# Step 5: Perform Inference
with torch.no_grad():
    logits = model(input_values).logits

# Step 6: Decode the Prediction
predicted_ids = torch.argmax(logits, dim=-1)
transcription = processor.batch_decode(predicted_ids)[0]

# Step 7: Print the Transcription
print("Transcription:", transcription)

